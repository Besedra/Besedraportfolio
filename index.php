<script>
    document.addEventListener("DOMContentLoaded", function() {
        const contactForm = document.querySelector('#contactForm');
        const btn = document.querySelector('#btn');
        const modal = new bootstrap.Modal(document.getElementById('messageModal'));
        const modalMessage = document.getElementById('modalMessage');

        contactForm.addEventListener('submit', function(e) {
            e.preventDefault();
            btn.innerHTML = '<i class="fa fa-spinner fa-spin"></i> Envoi en cours...';
            btn.disabled = true;

            const formData = new FormData(this);

            fetch('sendmail.php', {
                method: 'POST',
                body: formData
            })
            .then(response => response.text())
            .then(text => {
                if (text.trim() === 'success') {
                    modalMessage.innerHTML = '<div class="text-success"><i class="fa fa-check-circle"></i> Votre message a été envoyé avec succès. Merci!</div>';
                } else {
                    modalMessage.innerHTML = '<div class="text-danger"><i class="fa fa-exclamation-circle"></i> Erreur lors de l\'envoi. Veuillez réessayer.</div>';
                }
                modal.show();
            })
            .catch(error => {
                modalMessage.innerHTML = '<div class="text-danger"><i class="fa fa-times-circle"></i> Une erreur s\'est produite. Veuillez réessayer plus tard.</div>';
                modal.show();
            })
            .finally(() => {
                btn.innerHTML = '<i class="fa fa-paper-plane"></i> Envoyer';
                btn.disabled = false;
                contactForm.reset();
            });
        });
    });
</script>

